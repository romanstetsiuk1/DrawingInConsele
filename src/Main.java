import sun.java2d.loops.DrawLine;

public class Main {

//    Method draw line witch size 10
    public static void DrawLine () {
        for (int i = 0; i < 10; i++ ) {
            System.out.print("*");
        }
        System.out.println();
    }



    public static void main(String[] args) {
        DrawLine();
    }

}
